#include "libft.h"

int	ft_isquoted(char c, int *q)
{
	int	prev;

	prev = *q;
	if (c == '\'' || c == '\"')
	{
		if (!*q)
			*q = c;
		else if (c == *q)
			*q = 0;
	}
	if (prev != *q)
		return (1);
	else
		return (0);
}
