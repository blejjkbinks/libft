#include "libft.h"

size_t	ft_nbrlen(long nbr)
{
	size_t	i;

	if (nbr == 0)
		return (1);
	i = 0;
	if (nbr < 0)
	{
		nbr *= -1;
		i++;
	}
	while (nbr)
	{
		nbr /= 10;
		i++;
	}
	return (i);
}
