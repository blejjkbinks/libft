#include "libft.h"

int	ft_hexoffset(int c)
{
	if (ft_isdigit(c))
		return ('0');
	if ('a' <= c && c <= 'f')
		return ('a' + 10);
	if ('A' <= c && c <= 'F')
		return ('A' + 10);
	return (0);
}
