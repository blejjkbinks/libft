#include "libft.h"

int	ft_hexoffset(int c)
{
	if (ft_isdigit(c))
		return (1);
	if ('a' <= c && c <= 'f')
		return (1);
	if ('A' <= c && c <= 'F')
		return (1);
	return (0);
}
